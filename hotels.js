//hotel 1
let hotel1 = document.getElementById('hotel1');
hotel1.addEventListener('mouseover', logMouseOver1);
hotel1.addEventListener('mouseout', logMouseOut1);
function logMouseOver1() {
    hotel1.style.color = "black";
    hotel1.style.boxShadow = "10px 20px 30px black";
}

function logMouseOut1() {
    hotel1.style.color = "gray";
    hotel1.style.boxShadow = "0px 0px 0px lightblue";
}

