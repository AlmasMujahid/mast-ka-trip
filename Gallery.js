//image1
let image1 = document.getElementById("img1");
img1.addEventListener('mouseover',logMouseOver1);
img1.addEventListener('mouseout', logMouseOut1);
function logMouseOver1() {
    img1.style.color = "black";
    img1.style.boxShadow = "10px 20px 30px black";
}
function logMouseOut1() {
    img1.style.color = "gray";
    img1.style.boxShadow = "0px 0px 0px lightblue"; 
}

